import logging

from tuxemon.core.npc import NPC

logger = logging.getLogger(__name__)


# Class definition for the player.
class Player(NPC):
    """ Object for Players.  WIP
    """

    def __init__(self, npc_slug):
        super().__init__(npc_slug)
        self.isplayer = True

        # Game variables for use with events
        self.game_variables = {'steps': 0}

    def move(self, time_passed_seconds):
        """ Move the player around the game world
            Increment the number of steps

        :param time_passed_seconds: A float of the time that has passed since the last frame.
            This is generated by clock.tick() / 1000.0.

        :type time_passed_seconds: Float
        """
        # TODO: this will also record involuntary steps.
        # refactor so that only movements from the player are recorded.
        before_x = self.tile_pos[0]
        before_y = self.tile_pos[1]

        NPC.move(self, time_passed_seconds)

        after_x = self.tile_pos[0]
        after_y = self.tile_pos[1]

        diff_x = abs(after_x - before_x)
        diff_y = abs(after_y - before_y)

        self.game_variables['steps'] += diff_x + diff_y
